#pragma once

#include <vector>
#include <cstddef>
#include <cstdint>
#include <numeric>   // Для std::reduce
#include <bit>       // Для std::popcount (C++20)

/**
 * @file node_types.hpp
 * @brief Классификатор специализированных узлов полярного дерева для Fast-SSC декодера.
 *
 * Содержит перечисление поддерживаемых типов узлов и оптимизированную функцию 
 * для их распознавания «на лету» без изменения архитектуры вызовов.
 */

/**
 * @brief Классы узлов полярного дерева, используемые для оптимизации алгоритма Fast-SSC.
 *
 * Каждый тип узла представляет собой специфическую структуру распределения информационных
 * и замороженных битов, для которой существуют прямые математические формулы декодирования.
 */
enum class NodeType {
    Generic, ///< Смешанный узел (требует классического рекурсивного деления на f и g функции)
    Rate0,   ///< Полностью замороженный узел (все биты поддерева равны 0, кодовое слово состоит из +1)
    Rate1,   ///< Полностью информационный узел (декодируется жестким пороговым решением по знаку LLR)
    Rep      ///< Узел повторения (все биты заморожены, кроме последнего; решение зависит от суммы всех LLR)
};

/**
 * @brief Оптимизированное определение типа узла полярного дерева на основе маски заморозки подканалов.
 *
 * Функция подсчитывает количество активных информационных битов в заданном сегменте 
 * глобальной маски и сопоставляет результат с известными паттернами Fast-SSC. 
 * 
 * @note Вычисления оптимизированы с помощью std::reduce для исключения ветвления инструкций (if-условий) 
 * inside цикла, что позволяет компилятору автоматически векторизовать (SIMD) процесс подсчета.
 *
 * @param[in] leaf_offset Индекс смещения (начальная позиция) текущего подблока в глобальной маске листьев.
 * @param[in] n Длина текущего подблока (размер оцениваемого поддерева, степень двойки).
 * @param[in] bit_mask Глобальная полярная маска по стандарту 5G NR (1 — INFORMATION, 0 — FROZEN).
 * @return NodeType Выявленный класс узла, определяющий дальнейшую стратегию декодирования подблока.
 */
inline NodeType determine_node_type(size_t leaf_offset, size_t n, const std::vector<uint8_t>& bit_mask) {
    // Векторизованный подсчет единиц в диапазоне маски. 
    // Поскольку элементы равны строго 0 или 1, сумма элементов эквивалентна числу инфо-битов.
    const size_t info_count = std::reduce(bit_mask.begin() + leaf_offset, 
                                          bit_mask.begin() + leaf_offset + n, 
                                          size_t(0));

    if (info_count == 0) {
        return NodeType::Rate0;
    }
    if (info_count == n) {
        return NodeType::Rate1;
    }

    // Проверка на узел повторения (Repetition Node): в подблоке ровно один инфо-бит, 
    // и он обязан располагаться на самой последней позиции данного поддерева.
    if (info_count == 1 && bit_mask[leaf_offset + n - 1] == 1) {
        return NodeType::Rep;
    }

    return NodeType::Generic;
}
