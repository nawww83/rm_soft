#pragma once

#include <vector>
#include <cstddef>
#include <cstdint>

/**
 * @file node_types.hpp
 * @brief Блок классификации специализированных узлов бинарного дерева полярного кода.
 * 
 * Содержит перечисление типов узлов для оптимизации Fast-SSC (Fast Simplified Successive Cancellation)
 * и функцию для их автоматического определения на основе маски замороженных подканалов.
 */

/**
 * @brief Перечисление типов узлов полярного дерева для алгоритма Fast-SSC.
 * 
 * Позволяет декодеру распознавать структуры поддеревьев, для которых существуют 
 * аналитические формулы мгновенного вычисления кодовых слов без побитового спуска к листьям.
 */
enum class NodeType {
    Generic, ///< Классический смешанный узел (требует дальнейшего рекурсивного деления f и g)
    Rate0,   ///< Полностью замороженный узел (все биты в поддереве гарантированно равны 0)
    Rate1,   ///< Полностью информационный узел (все биты поддерева содержат полезные данные)
    Rep      ///< Узел повторения (все биты в поддереве заморожены, кроме самого последнего)
};

/**
 * @brief Определение типа узла полярного дерева на основе маски заморозки подканалов.
 * 
 * Функция анализирует заданный сегмент глобальной маски и сопоставляет его структуру 
 * со свойствами специальных узлов Fast-SSC. Это критически снижает вычислительную задержку декодера.
 * 
 * @param[in] leaf_offset Индекс смещения (позиция начала) текущего подблока в глобальной маске листьев.
 * @param[in] n Длина текущего подблока (размер оцениваемого поддерева, степень двойки).
 * @param[in] bit_mask Глобальная полярная маска по стандарту 5G NR (1 — INFORMATION, 0 — FROZEN).
 * @return NodeType Выявленный класс узла, определяющий дальнейшую стратегию декодирования подблока.
 */
inline NodeType determine_node_type(size_t leaf_offset, size_t n, const std::vector<uint8_t>& bit_mask) {
    size_t info_count = 0;
    for (size_t i = 0; i < n; ++i) {
        if (bit_mask[leaf_offset + i] == 1) info_count++;
    }

    if (info_count == 0) return NodeType::Rate0;
    if (info_count == n) return NodeType::Rate1;

    // Проверка на Repetition Node (все 0, последний 1)
    if (info_count == 1 && bit_mask[leaf_offset + n - 1] == 1) {
        return NodeType::Rep;
    }

    return NodeType::Generic;
}
