#pragma once

#include <cmath>

/**
 * @file qam16.hpp
 * @brief Модуль модуляции и вычисления мягких решений (LLR) для созвездия QAM-16 / PAM-4.
 * 
 * Блок реализует отображение пар битов на одномерное созвездие PAM-4 с использованием 
 * кодирования Грея, а также высокоэффективный геометрический демодулятор (BICM-демодуляция).
 * Благодаря свойствам кода Грея, вычисление LLR производится независимо для каждого бита
 * на основе расстояний до решающих границ, без ресурсоемкого перебора точек созвездия.
 */

/**
 * @brief Нормировочный коэффициент для созвездия QAM-16 / PAM-4.
 * 
 * Обеспечивает единичную среднюю энергию сигнального созвездия (\f$E_{avg} = 1\f$).
 * Вычисляется исходя из средних квадратов амплитуд одномерных точек PAM-4:
 * \f$E_{avg} = \frac{(-3)^2 + (-1)^2 + 1^2 + 3^2}{4} = \frac{20}{4} = 5\f$.
 * Для двухмерного созвездия QAM-16 средняя энергия равна \f$5 + 5 = 10\f$, откуда нормировочный делитель равен \f$\sqrt{10}\f$.
 */
const double QAM16_NORM = std::sqrt(10.0); 

/**
 * @brief Отображение пары битов на одномерную сигнальную координату PAM-4 (кодирование Грея).
 * 
 * Распределение точек выполнено так, чтобы любые два соседних символа отличались строго в одном бите:
 * - (0, 0) \f$\to -3/\sqrt{10}\f$
 * - (0, 1) \f$\to -1/\sqrt{10}\f$
 * - (1, 1) \f$\to +1/\sqrt{10}\f$
 * - (1, 0) \f$\to +3/\sqrt{10}\f$
 * 
 * @param[in] b0 Первый бит пары (старший значащий бит, MSB).
 * @param[in] b1 Второй бит пары (младший значащий бит, LSB).
 * @return double Нормированная координата PAM-4 на фазовой плоскости.
 */
inline double map_bit_pair_to_pam4(int b0, int b1) {
    if (b0 == 0 && b1 == 0) return -3.0 / QAM16_NORM;
    if (b0 == 0 && b1 == 1) return -1.0 / QAM16_NORM;
    if (b0 == 1 && b1 == 1) return 1.0 / QAM16_NORM;
    return 3.0 / QAM16_NORM; 
}

/**
 * @brief Независимое вычисление LLR для битов PAM-4 на основе геометрии кода Грея.
 * 
 * Вместо классического перебора всех точек созвездия (алгоритм Max-Log-MAP), 
 * данная функция использует свойство кода Грея, позволяющее детектировать биты независимо:
 * - Для бита \f$b_0\f$ существует единственная решающая граница в нуле. Метрика \f$raw\_llr\_b0\f$ 
 *   линейно зависит от удаления от этой границы.
 * - Для бита \f$b_1\f$ область логической единицы симметрично зажата в интервале \f$[-2, 2]\f$. 
 *   Использование функции модуля позволяет мгновенно вычислить расстояние до двух зеркальных решающих границ.
 * 
 * @note **Соглашение о знаках 3GPP:** Положительное значение \f$LLR \ge 0\f$ означает большую 
 *       вероятность логического нуля `0`, отрицательное \f$LLR < 0\f$ — логической единицы `1`.
 * 
 * @param[in] y Принятая из канала связи вещественная координата (содержит шум AWGN).
 * @param[in] sigma2_1d Дисперсия шума в канале в расчете на одно измерение (одну координатную ось).
 * @param[out] llr_b0 Мягкое решение для бита \f$b_0\f$ (положительное при `0`, отрицательное при `1`).
 * @param[out] llr_b1 Мягкое решение для бита \f$b_1\f$ (положительное при `0`, отрицательное при `1`).
 */
inline void calculate_pam4_llr(double y, double sigma2_1d, double& llr_b0, double& llr_b1) {
    double y_scaled = y * QAM16_NORM;
    double raw_llr_b0 = -y_scaled;
    double raw_llr_b1 = std::abs(y_scaled) - 2.0;
    
    // Масштабирование метрик правдоподобия с учетом текущей мощности шума в канале.
    // Критически важно для корректной работы мягкого полярного декодера.
    double noise_factor = QAM16_NORM / sigma2_1d; 

    llr_b0 = raw_llr_b0 * noise_factor;
    llr_b1 = raw_llr_b1 * noise_factor;
}
